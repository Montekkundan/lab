def plot_pos_neg(img_emb_np, txt_emb_np, title="Pos vs neg cosine similarity"):
  sim = img_emb_np @ txt_emb_np.T
  pos = np.diag(sim)
  neg = sim[~np.eye(sim.shape[0], dtype=bool)]

