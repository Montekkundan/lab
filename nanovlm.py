def draw_sample(color, shape, position, img_size = IMG_SIZE):
  img = Image.new('RGB', (img_size, img_size), 'white')
  draw = ImageDraw.Draw(img)

  margin = 6
  h = w = img_size - 2*margin

  # x coordingates
  if 'left' in position:
    x0 = margin
    x1 = margin + w//2
  elif 'top-left' in position:
    x0 = margin
    x1 = margin + w//2
  elif 'bottom-left' in position:
    x0 = margin
    x1 = margin + w//2
  elif 'right' in position:
    x0 = margin + w//2
    x1 = img_size - margin
  elif 'top-right' in position:
    x0 = margin + w//2
    x1 = img_size - margin
  elif 'bottom-right' in position:
    x0 = margin + w//2
    x1 = img_size - margin
  else:
    x0 = margin + w//4
    x1 = margin + 3 * w//4

  # y coordingates
  if 'top' in position:
    y0 = margin
    y1 = margin + h//2
  elif 'top-left' in position:
    y0 = margin
    y1 = margin + h//2
  elif 'top-right' in position:
    y0 = margin
    y1 = margin + h//2
  elif 'bottom' in position:
    y0 = margin + h//2
    y1 = img_size - margin
  elif 'bottom-left' in position:
    y0 = margin + h//2
    y1 = img_size - margin
  elif 'bottom-right' in position:
    y0 = margin + h//2
    y1 = img_size - margin
  else:
    y0 = margin + h//4
    y1 = margin + 3 * h//4

  if shape == 'square':
    draw.rectangle([x0, y0, x1, y1], fill = color, outline='black')
  elif shape == 'circle':
    draw.ellipse([x0, y0, x1, y1], fill = color, outline='black')
  else: #triangle
    draw.polygon([((x1+x0)//2, y0), (x0, y1), (x1, y1)], fill = color, outline='black')

  return img

class ShapesDataset():
  def __init__(self):
    self.images = []
    self.captions = []

    for c in colors:
      for s in shapes:
        for p in positions:
          img = draw_sample(c, s, p)
          cap = f'{c} {s} {p}'

          self.images.append(torch.from_numpy(np.array(img)).permute(2, 0, 1).float()/255.0)
          self.captions.append(cap)

    self.vocab, self.word2idx = self.build_vocab(self.captions)

  def build_vocab(self, texts):
    words = sorted({w for t in texts for w in t.split()})
    vocab = ['[CLS]'] + words
    w2i = {w: i for i, w in enumerate(vocab)}
    return vocab, w2i

  def encode_text(self, text):
    toks = [self.word2idx['[CLS]']] + [self.word2idx[w] for w in text.split()]
    return torch.tensor(toks, dtype=torch.long)

  def __len__(self):
    return len(self.images)

  def __getitem__(self, idx):
    return self.images[idx], self.encode_text(self.captions[idx]), self.captions[idx]

