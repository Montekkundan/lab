import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'
import {
  experimentsConfig,
  getExperimentImportPath,
  getExperimentDisplayTitle,
  isExperimentPublished,
} from '../src/lib/experiments.config'

const __filename = fileURLToPath(import.meta.url)
const __dirname = path.dirname(__filename)

const importMapOutputPath = path.join(__dirname, '../src/lib/experiments.import-map.ts')
const experimentsJsonOutputPath = path.join(__dirname, '../public/experiments.json')

/**
 * Main function to generate the experiment import map
 */
function generateExperimentImportMap() {
  try {
    fs.writeFileSync(importMapOutputPath, generateImportMapFile())
    fs.writeFileSync(experimentsJsonOutputPath, generateExperimentsJsonFile())

    console.log(`Successfully generated experiment import map and experiments JSON feed`)
  } catch (error) {
    console.error('Error generating experiments import map:', error)
  }
}

function generateImportMapFile() {
  const lines = experimentsConfig.map((experiment) => {
    const importPath = getExperimentImportPath(experiment)
    return `  '${experiment.slug}': () => import('@/experiments/${importPath}'),`
  })

  return `// This file is auto-generated by scripts/generate-experiments.ts
// Do not edit manually.

import type { ComponentType } from 'react'

export type ExperimentComponent = ComponentType<Record<string, unknown>>
export type ExperimentModule = {
  default: ExperimentComponent
}
export type ExperimentLoader = () => Promise<ExperimentModule>

export const experimentImportMap = {
${lines.join('\n')}
} satisfies Record<string, ExperimentLoader>
`
}

function generateExperimentsJsonFile() {
  const experiments = experimentsConfig
    .filter(isExperimentPublished)
    .slice()
    .sort((a, b) => new Date(b.createdAt).getTime() - new Date(a.createdAt).getTime())
    .map((experiment) => ({
      slug: experiment.slug,
      title: getExperimentDisplayTitle(experiment.slug),
      description: experiment.description ?? '',
      tags: experiment.tags ?? [],
      createdAt: experiment.createdAt,
      href: `/experiments/${experiment.slug}`,
      og: experiment.og ?? '',
    }))

  return `${JSON.stringify(
    {
      generatedAt: new Date().toISOString(),
      count: experiments.length,
      experiments,
    },
    null,
    2
  )}\n`
}

generateExperimentImportMap()
